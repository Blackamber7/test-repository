public class Animale {
    private int x;
    private int y;
    private int z;
    private int u;

    public Animale() {
    }

    public Animale(int x, int y, int z, int u) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.u = u;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public int getU() {
        return u;
    }

    public void setU(int u) {
        this.u = u;
    }

   
    
}
